        <!-- Header -->
        <h1 class="text-4xl font-bold text-center text-indigo-600 dark:text-indigo-400 mb-8">
            Christians should meet to reason
        </h1>

        <!-- Intro -->
        <p class="text-lg mb-6">
            The way Christian churches glorify preachers these days sets them up like they’ve got the final word on everything—deliberations, interpretations, how to apply scripture. It’s messed up. We should be figuring this stuff out ourselves, not bowing to some guy who acts like he’s the ultimate authority. A preacher isn’t a know-it-all, my-way-or-the-highway, I-run-this-show figure. He’s just one of many God’s gifted to teach. That’s it.
        </p>

        <!-- Section: Multiple Teachers -->
        <h2 class="text-2xl font-semibold text-gray-800 dark:text-gray-200 mt-8 mb-4">
            Church Shouldn’t Be a One-Man Show
        </h2>
        <p class="mb-4">
            In a church, there ought to be multiple teachers—people God’s clearly blessed with the ability to break down and explain scripture. Theoretically, that could be every single member. The Bible’s clear: different people get different gifts. Point is, we should all be reasoning through scripture together. It’s not supposed to be a handful of folks telling the rest of us what the Bible says and means. By God’s grace, we can read it too. We can think too. Their take isn’t the end-all-be-all.
        </p>
        <blockquote class="border-l-4 border-indigo-500 pl-4 italic text-gray-700 dark:text-gray-300 mb-4">
            “And when certain were hardened and were disbelieving, speaking evil of the Way before the multitude, he, having departed from them, did separate the disciples, reasoning every day in the school of a certain Tyrannus.” — Acts 19:9 (YLT)
        </blockquote>
        <p class="mb-4">
            Paul didn’t just lecture and call it a day—he <em>reasoned</em> with them, daily, in a setting open to discussion. That’s the vibe we’re missing.
        </p>

        <!-- Section: The Preacher Myth -->
        <h2 class="text-2xl font-semibold text-gray-800 dark:text-gray-200 mt-8 mb-4">
            “The Preacher”? More Like a Scam
        </h2>
        <p class="mb-4">
            But what happens now? When there’s confusion about what the Bible says on something, there’s a little chatter here, some debate there, and then it’s, “Oh, the preacher will give a teaching on it.” So he does—drops his sermon, answers a few questions, and boom, that’s the final word. What he says goes. That’s not how it’s meant to be. And what’s with this “the preacher” nonsense? “An ordained preacher”? Show me that in the Bible. It’s a con. Sure, we had Timothy, Titus, and others—guys Paul sent out—but they weren’t strutting around like today’s “preachers.”
        </p>

        <!-- Section: Extortion Example -->
        <h2 class="text-2xl font-semibold text-gray-800 dark:text-gray-200 mt-8 mb-4">
            Extortion in the Name of God
        </h2>
        <p class="mb-4">
            Imagine a church paying their “preacher” 150,000 naira a month. That’s 1,800,000 a year. Then they cover his rent too—450,000 naira a month. Add it up: 2,250,000 a year. Break it down, and they’re shelling out roughly 43,200 a week for this guy (2,250,000 ÷ 52 weeks). Meanwhile, their weekly collection averages 27,000 naira. This is a real church—Church of Christ in Abuja.
        </p>
        <p class="mb-4">
            You do the math. How’s this place staying afloat? Easy—every time a member’s in need, they guilt-trip the congregation into donating more. But touch the church purse? Nah, they won’t dare. You can see why. It’s extortion, plain and simple.
        </p>
        <p class="mb-4">
            Every Sunday, they’re told to give—“the collection for the saints,” they call it. Except it’s really for one saint: the “preacher.” What a scam. And this isn’t just that church—who knows how many others are pulling the same stunt? And some guys have a seminar coming up about an “elevated pulpit.” Hilarious.
        </p>

        <!-- Section: Back to Basics -->
        <h2 class="text-2xl font-semibold text-gray-800 dark:text-gray-200 mt-8 mb-4">
            Ditch the Pulpit, Bring Back the Bible Way
        </h2>
        <p class="mb-4">
            We don’t need a fancier stage. We need reasoning, discussion, Christians coming together to dig into the truth ourselves—not some dude barking at us to swallow what he thinks is right just because he’s got a Bible school degree. Don’t even get me started on Bible school. This isn’t about a man. Follow no man—only God, only Jesus. We already know G.O.s (General Overseers) are a problem, but preachers are turning into the same thing.
        </p>
        <blockquote class="border-l-4 border-indigo-500 pl-4 italic text-gray-700 dark:text-gray-300 mb-4">
            “And on the first of the week, the disciples having been gathered together to break bread, Paul was discoursing to them, about to depart on the morrow, and he continued the discourse till midnight.” — Acts 20:7 (YLT)
        </blockquote>
        <p class="mb-4">
            They met, they broke bread, they talked—Paul wasn’t some untouchable figure on a pedestal; it was a gathering, a conversation. If we just did it like they did back then—meeting house to house—Christians wouldn’t be drowning in these administrative messes. That’s how it should be.
        </p>

